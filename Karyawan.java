package Main;

public class Karyawan {
	public String code, name, gender, position;
	public long wages;
	
	public Karyawan(String code, String name, String gender, String position, long wages) {
		super();
		this.code = code;
		this.name = name;
		this.gender = gender;
		this.position = position;
		this.wages = wages;
	}

}
